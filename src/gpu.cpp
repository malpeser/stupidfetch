// getGpuFast - not much better
std::vector<std::string> getGpuFast() {
    std::vector<std::string> gpus;
    
    for (const auto& entry : std::filesystem::directory_iterator("/sys/class/drm/")) {
        std::string cardName = entry.path().filename();
        if (cardName.find("card") != 0) continue; // F8key f8ke f8kes!!!!!!!!
        
        std::string devicePath = entry.path().string() + "/device/";
        std::string vendor = readFileTrim(devicePath + "vendor");
        std::string device = readFileTrim(devicePath + "device");
        
        if (vendor == "0x8086")
        {
            std::string pcidev = getDeviceIntel(device);
            gpus.push_back("Intel Corporation "+pcidev);
        }
        else if (vendor == "0x10de")
        {
            std::string pcidev = getDeviceNVIDIA(device);
            gpus.push_back("NVIDIA "+pcidev);
        } 
        else if (vendor == "0x1002")
        {
            std::string pcidev = getDeviceAMD(device);
            gpus.push_back("AMD "+pcidev);

        }
    }
    
    return gpus;
}


// getGpu - Get every GPU in system and pack it up in a nice std::vector
std::vector<std::string> getGpu(int fast) 
{
    std::vector<std::string> gpus;
    if (fast == 1) {
        gpus = getGpuFast();
        return gpus;
    }

    std::string output = exec("lspci -mm -d ::0300:");

    std::istringstream iss(output);
    std::string line;

    while (std::getline(iss, line))
    {
        if (line.find("VGA compatible controller") == std::string::npos && line.find("3D controller") == std::string::npos) continue;

        size_t pos = 0;
        std::vector<std::string> fields;
        while (true)
        {
            size_t start = line.find('"', pos);
            if (start == std::string::npos) break;
            size_t end = line.find('"', start + 1);
            if (end == std::string::npos) break;
            fields.push_back(line.substr(start + 1, end - start - 1));
            pos = end + 1;
        }

        if (fields.size() < 4) continue;

        std::string vendor = fields[1];
        std::string device = fields[2];

        if (vendor == "Corporation") vendor = "Intel";
        else if (vendor == "NVIDIA Corporation") vendor = "NVIDIA";
        else if (vendor == "Advanced Micro Devices, Inc. [AMD/ATI]") vendor = "AMD";

        std::string gpu_name;
        size_t open = device.find('[');
        size_t close = device.find(']');
        if (open != std::string::npos && close != std::string::npos && close > open) gpu_name = device.substr(open + 1, close - open - 1); else gpu_name = device;

        gpus.push_back(vendor + " " + gpu_name);
    }

    return gpus;
}